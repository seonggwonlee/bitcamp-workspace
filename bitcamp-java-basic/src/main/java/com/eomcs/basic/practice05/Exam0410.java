package com.eomcs.basic.practice05;

public class Exam0410 {

  //흐름 제어문 - for 반복문
  public static void main(String[] args) {

    for (int i = 1; i <=5; i++)
      System.out.println(i);

    // for문의 전형적인 기본 예
    // int i = 1 --> 1번째 실행
    // i <= 5; --> 2번째 실행
    // System.out.println(i); --> 3번째 실행
    // i++; --> 4번째 실행
    // 그 후 2 > 3 > 4 반
  }
}
