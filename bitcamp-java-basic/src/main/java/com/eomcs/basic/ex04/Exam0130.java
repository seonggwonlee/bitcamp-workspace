package com.eomcs.basic.ex04;

public class Exam0130 {

  public static void main (String[] args) {
    
    // 변수에는 값(변수 또는 리터럴)을 할당할 수가 있다.\
    
    int age;
    
    age = 20; // 변수에 값 할당
    
    // age는 L-value, 20은 R-value이다. L-value는 리터럴이 될  없다.
    
    // '='는 할당(배정)연산자이며 오른쪽에 지정된 값을 왼쪽 변수가 가리키는 메모리에 저장한다.
  }
}
